class Calculator():
    def start(self):
        pass

    def add(self,num1,num2):
        return num1+num2

    def subtract(self,num1,num2):
        return num1 - num2

    def multiply(self,num1,num2):
        return num1 * num2

    def divide(self,num1,num2):
        return num1/num2

if __name__ == "__main__":
    myApp = Calculator()
    myApp.start()




