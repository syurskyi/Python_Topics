x = [2, 8, 1, 4, 6, 3, 7]

print("Sorted List returned :",)
print(sorted(x))

print("\nReverse sort :",)
print(sorted(x, reverse = True))

print("\nOriginal list not modified :",)
print(x)

# Output :
# Sorted List returned : [1, 2, 3, 4, 6, 7, 8]
#
# Reverse sort : [8, 7, 6, 4, 3, 2, 1]
#
# Original list not modified : [2, 8, 1, 4, 6, 3, 7]
